// Very simple, given an integer or a floating-point number, find its opposite.

// Examples:

// 1: -1
// 14: -14
// -34: 34

// solution

//  opposite: This is the name of the function.
// number =>: This part defines a function that takes one parameter, named number.
// -number: The function returns the negation of the number parameter.

opposite = number => -number
  